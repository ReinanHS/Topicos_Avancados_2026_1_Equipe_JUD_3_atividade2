-- 2. Tabela de Datasets
CREATE TABLE datasets (
    id_dataset bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome text NOT NULL UNIQUE,
    url_origem text,
    dominio text NOT NULL,
    tipo_tarefa text NOT NULL CHECK (tipo_tarefa IN ('discursiva', 'multipla_escolha')),
    versao text,
    descricao text,
    created_at timestamptz NOT NULL DEFAULT now ()
);

COMMENT ON TABLE datasets IS 'Conjuntos de dados de avaliação (benchmarks).';

COMMENT ON COLUMN datasets.id_dataset IS 'Identificador único do dataset.';

COMMENT ON COLUMN datasets.nome IS 'Nome do dataset.';

COMMENT ON COLUMN datasets.url_origem IS 'URL da origem do dataset.';

COMMENT ON COLUMN datasets.dominio IS 'Domínio do dataset.';

COMMENT ON COLUMN datasets.tipo_tarefa IS 'Tipo de tarefa: discursiva ou múltipla escolha.';

COMMENT ON COLUMN datasets.versao IS 'Versão do dataset.';

COMMENT ON COLUMN datasets.descricao IS 'Descrição do dataset.';

COMMENT ON COLUMN datasets.created_at IS 'Data de criação do dataset.';